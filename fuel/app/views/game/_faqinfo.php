<strong>What is VG Unstuck&#63;</strong><br>
<p>VG Unstuck is essentially a web application that lets you track the progress of your video games in a table. Whenever you select a status of &quot;Stuck&quot; the website will provide an &quot;Unstuck Me!&quot; button that will allow you find YouTube videos based on your comments to help you out.</p>

<strong>Why are my search results not very helpful&#63;</strong>
<p>The &quot;Unstuck Me&quot; feature uses keywords from your comments from that particular game's entry to search for videos. Try using fewer words like you would in a search enginge i.e. &quot;final dungeon.&quot;</p>

